package com.yichuang.fuyang.entity.notice;

import java.io.Serializable;
import java.util.Date;

/**
 * 通知实体类
 * @author zhuoming
 */
public class Notice implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -4708358834298344357L;
	//通知记录主键
	private int noticeId;
	//通知编码
	private String noticeCode;
	//通知类型
	private String noticeType;
	//通知内容
	private String noticeContent;
	//通知描述
	private String noticeDesc;
	//通知创建时间
	private Date createTime;
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public int getNoticeId() {
		return noticeId;
	}
	public void setNoticeId(int noticeId) {
		this.noticeId = noticeId;
	}
	public String getNoticeCode() {
		return noticeCode;
	}
	public void setNoticeCode(String noticeCode) {
		this.noticeCode = noticeCode;
	}
	public String getNoticeType() {
		return noticeType;
	}
	public void setNoticeType(String noticeType) {
		this.noticeType = noticeType;
	}
	public String getNoticeContent() {
		return noticeContent;
	}
	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}
	public String getNoticeDesc() {
		return noticeDesc;
	}
	public void setNoticeDesc(String noticeDesc) {
		this.noticeDesc = noticeDesc;
	}
	@Override
	public String toString() {
		return "Notice [noticeId=" + noticeId + ", noticeCode=" + noticeCode + ", noticeType=" + noticeType
				+ ", noticeContent=" + noticeContent + ", noticeDesc=" + noticeDesc + "]";
	}
}
