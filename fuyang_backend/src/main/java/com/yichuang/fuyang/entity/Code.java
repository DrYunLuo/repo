package com.yichuang.fuyang.entity;

import java.io.Serializable;

public class Code implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1248757389197082673L;
	
	private String codeId;
	private String codeType;
	private String codeValue;
	private String codeValueName;
	public String getCodeId() {
		return codeId;
	}
	public void setCodeId(String codeId) {
		this.codeId = codeId;
	}
	public String getCodeType() {
		return codeType;
	}
	public void setCodeType(String codeType) {
		this.codeType = codeType;
	}
	public String getCodeValue() {
		return codeValue;
	}
	public void setCodeValue(String codeValue) {
		this.codeValue = codeValue;
	}
	public String getCodeValueName() {
		return codeValueName;
	}
	public void setCodeValueName(String codeValueName) {
		this.codeValueName = codeValueName;
	}
	
	

}
